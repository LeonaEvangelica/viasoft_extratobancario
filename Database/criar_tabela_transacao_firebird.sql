-- Script de criação de tabela para Firebird 3.0 (32 bits)

CREATE TABLE TRANSACAO (
    ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DATATRANSACAO DATE NOT NULL,
    NOMEORIGEM VARCHAR(100) NOT NULL,
    NOMEDESTINO VARCHAR(100) NOT NULL,
    DESCRICAO VARCHAR(255),
    VALOR DECIMAL(18,2) NOT NULL,
    TIPO CHAR(1) NOT NULL CHECK (TIPO IN ('C', 'D')),
    SALDO DECIMAL(18,2) NOT NULL
);

-- Índices auxiliares opcionais
CREATE INDEX IDX_TRANSACAO_DATATRANSACAO ON TRANSACAO (DATATRANSACAO);
CREATE INDEX IDX_TRANSACAO_NOMEORIGEM ON TRANSACAO (NOMEORIGEM);
CREATE INDEX IDX_TRANSACAO_NOMEDESTINO ON TRANSACAO (NOMEDESTINO);